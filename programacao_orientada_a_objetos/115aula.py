# Ambiente virtuais em Python (venv)
# Um ambiente virtual carrega toda a sua instalação
# do Python para uma pasta no caminho escolhid. 
# Ao ativar um ambiente virtual, a instalação do
# ambiente virtual será usada.
# venv é o médulo que vamos usar para
# criar ambiente virtuais.
# Você pode dar o nome que preferir para um 
# ambient virtual, mas os mais comuns são:
# venv env .venv .env
